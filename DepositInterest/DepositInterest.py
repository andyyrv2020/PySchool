x = int(input("Въведете сумата на депозита: "))
p = int(input("Въведете процент на лихва: "))
y = int(input("Въведете желаната сума: "))
years = 0
while x < y:
    x += x * p / 100
    x = int(x * 100) / 100 
    years += 1
print("След", years, "години в депозита ще има не по-малко от", y, "лева.")
